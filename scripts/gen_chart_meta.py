#!/usr/bin/env python3
"""Generate the frontend's chart-metadata and time-field tables from Python.

``src/vestigo/agent/chart_meta.py`` and ``src/vestigo/db/_time_fields.py`` are
the source of truth; this script writes their TypeScript mirrors::

    frontend/src/components/viz/lib/chartMeta.ts
    frontend/src/components/viz/lib/timeFields.ts

Both outputs are committed, and ``tests/test_chart_meta.py`` asserts that
re-running this script produces no diff — so the two languages cannot drift
apart the way they did when each maintained its own chart-type table (which is
what let ``propose_chart`` accept a pie chart and render a bar).

Re-run after editing either source module::

    uv run python scripts/gen_chart_meta.py [--check]

``--check`` exits non-zero instead of writing when the outputs are stale.
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(REPO_ROOT / "src"))

from vestigo.agent.chart_meta import (  # noqa: E402
    CHART_META,
    CHART_TYPES,
    SCALES,
    DataKind,
)
from vestigo.db._time_fields import TIME_FIELD_SPECS  # noqa: E402

VIZ_LIB = REPO_ROOT / "frontend/src/components/viz/lib"
BANNER = (
    "// Generated by scripts/gen_chart_meta.py — do not edit by hand;\n"
    "// edit the Python source module and re-run `uv run python scripts/gen_chart_meta.py`.\n"
)


def _ts_string(value: str) -> str:
    """A double-quoted TS string literal (Prettier's quote style here)."""
    escaped = value.replace("\\", "\\\\").replace('"', '\\"')
    return f'"{escaped}"'


def _ts_string_array(values: tuple[str, ...] | list[str]) -> str:
    return "[" + ", ".join(_ts_string(v) for v in values) + "]"


def _ts_union(values: tuple[str, ...]) -> str:
    return " | ".join(_ts_string(v) for v in values)


def _wrap_comment(text: str, indent: str) -> str:
    """Render *text* as wrapped ``//`` comment lines at *indent*."""
    words, lines, current = text.split(), [], ""
    for word in words:
        candidate = f"{current} {word}".strip()
        if len(candidate) + len(indent) + 3 > 96:
            lines.append(current)
            current = word
        else:
            current = candidate
    if current:
        lines.append(current)
    return "".join(f"{indent}// {line}\n" for line in lines)


def render_chart_meta() -> str:
    out = [
        BANNER,
        "/**\n"
        " * Chart-type metadata: which scales of measurement each chart type suits,\n"
        " * which aggregation feeds it, and which options it actually reads. Shared by\n"
        " * the Visualize page's rail, the task presets, and the agent's chart\n"
        " * proposals — so an analyst and the agent are held to the same rules.\n"
        " */\n",
        'import type { ChartType, Scale } from "./chartConfig";\n\n',
        f"export type DataKind = {_ts_union(_data_kinds())};\n\n",
        "export const CHART_META: Record<\n"
        "  ChartType,\n"
        "  {\n"
        "    label: string;\n"
        "    scales: Scale[];\n"
        "    dataKind: DataKind;\n"
        "    /** Scale assumed when a chart request does not state one. */\n"
        "    defaultScale: Scale;\n"
        "    /** ChartOptions keys this type consumes; others are inert for it. */\n"
        "    readsOptions: string[];\n"
        "    supportsCompare: boolean;\n"
        "    /** Two-field charts (pivot/sankey/scatter) need a second field picked. */\n"
        "    requiresSecondField: boolean;\n"
        "  }\n"
        "> = {\n",
    ]
    for chart_type in CHART_TYPES:
        meta = CHART_META[chart_type]
        if meta.note:
            out.append(_wrap_comment(meta.note, "  "))
        out.append(
            f"  {chart_type}: {{\n"
            f"    label: {_ts_string(meta.label)},\n"
            f"    scales: {_ts_string_array(meta.scales)},\n"
            f"    dataKind: {_ts_string(meta.data_kind)},\n"
            f"    defaultScale: {_ts_string(meta.default_scale)},\n"
            f"    readsOptions: {_ts_string_array(meta.reads_options)},\n"
            f"    supportsCompare: {str(meta.supports_compare).lower()},\n"
            f"    requiresSecondField: {str(meta.requires_second_field).lower()},\n"
            f"  }},\n"
        )
    out.append("};\n\n")
    out.append(f"export const SCALES: Scale[] = {_ts_string_array(SCALES)};\n\n")
    out.append(
        "export const chartTypesFor = (s: Scale): ChartType[] =>\n"
        "  (Object.keys(CHART_META) as ChartType[]).filter((c) =>\n"
        "    CHART_META[c].scales.includes(s),\n"
        "  );\n"
    )
    return "".join(out)


def _data_kinds() -> tuple[str, ...]:
    """DataKind values in declaration order, deduped across chart types."""
    from typing import get_args

    return get_args(DataKind)


def render_time_fields() -> str:
    out = [
        BANNER,
        "/**\n"
        " * Virtual `time:` fields — time parts usable as ordinary field tokens.\n"
        " *\n"
        " * `domain` is the complete, naturally-ordered value list for a cyclical\n"
        " * part (every hour exists whether or not the data covers it), or null for\n"
        " * an unbounded part like a calendar date. `display` labels values whose\n"
        " * canonical form is opaque ('1' -> 'Mon'); it is presentation-only — the\n"
        " * canonical value is what lives in ChartConfig, URLs, saved charts and\n"
        " * filters, so relabelling never invalidates a stored chart.\n"
        " */\n",
        'import type { Scale } from "./chartConfig";\n\n',
        "export interface TimeFieldMeta {\n"
        "  label: string;\n"
        "  scale: Scale;\n"
        "  domain: string[] | null;\n"
        "  display: Record<string, string> | null;\n"
        "}\n\n",
        'export const TIME_FIELD_PREFIX = "time:";\n\n',
        "export const TIME_FIELDS: Record<string, TimeFieldMeta> = {\n",
    ]
    for token, spec in TIME_FIELD_SPECS.items():
        domain = _ts_string_array(spec.domain) if spec.domain is not None else "null"
        if spec.display is None:
            display = "null"
        else:
            pairs = ", ".join(f"{_ts_string(k)}: {_ts_string(v)}" for k, v in spec.display.items())
            display = "{ " + pairs + " }"
        out.append(
            f"  {_ts_string(token)}: {{\n"
            f"    label: {_ts_string(spec.label)},\n"
            f"    scale: {_ts_string(spec.scale)},\n"
            f"    domain: {domain},\n"
            f"    display: {display},\n"
            f"  }},\n"
        )
    out.append("};\n\n")
    out.append(
        "/** True when *token* names a virtual time field. */\n"
        "export const isTimeField = (token: string): boolean => token in TIME_FIELDS;\n\n"
        "/**\n"
        " * Human label for one value of *token*, falling back to the raw value.\n"
        " * Never feed the result back into a filter or ChartConfig — it is display\n"
        " * text, and only the canonical value round-trips.\n"
        " */\n"
        "export function timeFieldValueLabel(token: string, value: string): string {\n"
        "  return TIME_FIELDS[token]?.display?.[value] ?? value;\n"
        "}\n"
    )
    return "".join(out)


OUTPUTS = {
    VIZ_LIB / "chartMeta.ts": render_chart_meta,
    VIZ_LIB / "timeFields.ts": render_time_fields,
}


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--check",
        action="store_true",
        help="Exit non-zero if any output is stale, instead of rewriting it.",
    )
    args = parser.parse_args()

    stale: list[Path] = []
    for path, render in OUTPUTS.items():
        content = render()
        current = path.read_text(encoding="utf-8") if path.exists() else None
        if current == content:
            continue
        if args.check:
            stale.append(path)
            continue
        path.write_text(content, encoding="utf-8")
        print(f"wrote {path.relative_to(REPO_ROOT)}")

    if stale:
        for path in stale:
            print(f"STALE: {path.relative_to(REPO_ROOT)}", file=sys.stderr)
        print(
            "Run `uv run python scripts/gen_chart_meta.py` and commit the result.",
            file=sys.stderr,
        )
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
